// Fonction pour obtenir des suggestions basées sur la saisie
export const getSuggestions = (
  type: 'profession' | 'specialty' | 'skills' | 'formations' | 'licenses' | 'caces',
  query: string,
  limit: number = 10
): string[] => {
  let suggestions: string[] = [];
  
  switch (type) {
    case 'profession':
      suggestions = professionSuggestions;
      break;
    case 'specialty':
      suggestions = specialtySuggestions;
      break;
    case 'skills':
      suggestions = skillsSuggestions;
      break;
    case 'formations':
      suggestions = formationSuggestions;
      break;
    case 'licenses':
      suggestions = licensesSuggestions;
      break;
    case 'caces':
      suggestions = cacesSuggestions;
      break;
  }
  
  if (!query || query.length < 2) {
    return suggestions.slice(0, limit);
  }
  
  const normalizedQuery = query.toLowerCase();
  
  // Recherche exacte d'abord
  const exactMatches = suggestions.filter(item =>
    item.toLowerCase().startsWith(normalizedQuery)
  );
  
  // Recherche partielle
  const partialMatches = suggestions.filter(item =>
    !exactMatches.includes(item) &&
    item.toLowerCase().includes(normalizedQuery)
  );
  
  return [...exactMatches, ...partialMatches].slice(0, limit);
};